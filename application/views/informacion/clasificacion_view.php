	<h1>Clasificación</h1>
	<p>De conformidad con los principales conceptos o categorías utilizadas para clasificar instituciones hospitalarias, a continuación se clasifica el Hospital Dr. José Gregorio Hernández:</p>
	<ul>
		<li>De acuerdo al número de camas: Hospital Tipo II con una capacidad real y presupuestada de 93 camas y una capacidad máxima actual de 106 camas.</li>
		<li>De acuerdo a la capacidad: Hospital Mediano.</li>
		<li>De acuerdo a los servicios que posee: Hospital General  Completo  ya que posee las cuatro especialidades clínicas consideradas como básicas (Cirugía, Medicina, Pediatría y Obstetricia).</li>
		<li>De acuerdo a la estancia o duración de la permanencia de los pacientes en la institución: Hospital para Agudos.</li>
		<li>De acuerdo a la dependencia a la cual esta adscrito: Hospital Oficial Nacional.</li>
		<li>De acuerdo a la organización sanitaria: Hospital de Zona.</li>
		<li>De acuerdo a la docencia: Hospital Docente no Universitario.</li>
		<li>De acuerdo a la arquitectura: Hospital Monobloque Horizontal.</li>
	</ul>
