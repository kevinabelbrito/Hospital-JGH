	<h1>Departamentos y Servicios</h1>
	<p>Desde el punto de vista teórico y práctico, un departamento es cada una de las partes en que se divide una organización hospitalaria, con objetivos definidos y coordinados en beneficio del fin que se persigue con aquella. Cada departamento representa prácticamente una actividad definida o agrupamiento y conjunción de actividades afines que sumadas deben configurar el diseño global que caracteriza la programación de la organización. Para traducir estas funciones en actividades, los departamentos deben contar con el nivel operacional, que en la organización hospitalaria se denomina servicio, secciones o unidades específicas.</p>
	<p>Dentro de un hospital general completo, como el Hospital “Dr. José Gregorio Hernández”, existen cuatro grandes departamentos clínicos: Cirugía, Medicina, Gineco Obstetricia y Pediatría y a cada uno deben estar adscritos diferentes servicios, así mismo, además de los departamentos clínicos, dentro del Hospital funcionan otros departamentos. A continuación se señalan todos los departamentos existentes en el Hospital “Dr. José Gregorio Hernández” y los servicios adscritos a cada uno, así mismo se presenta su organigrama estructural aprobado en Comisión Técnica en el año 2002.</p>
