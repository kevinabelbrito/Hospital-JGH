	<h1>Recurso Humano del Hospital</h1>
	<h2>Cuerpo Médico de Residentes:</h2>
	<p>El hospital cuenta al presente con 12 médicos residentes distribuidos de la siguiente manera: 03 en el Servicio de Obstetricia, y 9 ubicados en las Emergencias de Adulto y Pediátrica. Este escenario conduce a diversas situaciones:</p>
	<ul>
		<li>La carga horaria de los residentes es consumada casi exclusivamente por sus actividades de guardias en las respectivas emergencias. Esto impide la presencia de residentes en actividades de algunas salas de hospitalización de Pediatría, Medicina Interna, Cirugía y Traumatología.</li>
		<li>Ante lo expuesto en el punto anterior se hace imposible la asignación adecuada de médicos residentes a los mencionados servicios, desarticulando el adecuado funcionamiento de los mismos y comprometiendo actividades tan significativas como la revista medica. Lo anterior igualmente quebranta la responsabilidad que como institución se asume al emplear médicos en etapas de formación, a quienes se les debería garantizar entre otras cosas: actualización y capacitación en las diferentes áreas médicas.</li>
		<li>Los turnos diurnos y nocturnos de las guardias de las Emergencias Pediátrica y de Adultos sólo pueden ser asumidos por un residente, el mismo debe realizar actividades de triaje, atención de emergencias, atención de pacientes ubicados en observación, atención de pacientes hospitalizados cuando así se requiera y ante la existencia de casos quirúrgicos, entrar a pabellón para asistir al cirujano de guardia en la respectiva intervención, dejando en esta ultima situación la emergencia sin medico por el lapso de tiempo que el residente de guardia deba permanecer en el área quirúrgica. Todo ello ocasiona congestionamiento de la emergencia, y por supuesto malestar de los usuarios quienes manifiestan constantemente su inconformidad con la atención que reciben.</li>
		<li>Dificultad para conceder vacaciones reglamentarias.</li>
		<li>Aumento de la carga horaria laborada por residentes ante la enfermedad o ante cualquier motivo que justifique la ausencia de alguno de ellos.</li>
	</ul>
	<h2>Cuerpo Medico de Especialistas:</h2>
	<p>Situaciones similares a las presentadas por el Cuerpo Medico de Residentes son las exhibidas por el Cuerpo Medico de Especialistas.</p>
	<p>En algunos servicios la cantidad de especialistas y sus respectivas cargas horarias reglamentarias sólo son suficientes para cubrir los turnos de guardias, quedando las actividades de salas de hospitalización y de consulta externa excluidas.</p>
	<p>Incluso en servicios como el de Cirugía, Traumatología y Gineco Obstetricia la carga horaria estipulada en la contratación de los respectivos especialistas no es suficiente para cubrir el rol mensual de guardias debiéndose asignar cargas horarias superiores a las estipuladas por sus contrataciones.</p>
	<p>Como medida paliativa se ha contado con la colaboración de especialistas de la Misión Medica Cubana en las áreas de Anestesiología, Cirugía, Pediatría, Gineco Obstetricia y Dermatología. No obstante, la coordinación de este personal pertenece a otras instancias, hecho que dificulta la programación de actividades con los mismos.</p>
	<p>Es sabido que en fecha 03 de Noviembre de 2005 fue aprobada la Gaceta Oficial Nº 38.306, en donde se especifica que los Médicos Especialistas podrán acogerse al régimen de contratación de seis (6) u ocho (8) horas diarias, lo que genera un monto mensual de disponibilidad del médico en la Institución de 120 horas o 160 horas mensuales respectivamente. Ante estas circunstancias, el déficit de médicos en algunas especialidades se hace aun más evidente.</p>
